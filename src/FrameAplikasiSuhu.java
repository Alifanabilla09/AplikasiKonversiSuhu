// Fungsi konversi dari Celcius
    private double konversiCelcius(double suhu, String ke) {
        switch (ke) {
            case "Fahrenheit":
                return (suhu * 9 / 5) + 32;
            case "Kelvin":
                return suhu + 273.15;
            case "Reamur":
                return suhu * 4 / 5;
            default:
                return suhu;
        }
    }

// Fungsi konversi dari Fahrenheit
    private double konversiFahrenheit(double suhu, String ke) {
        switch (ke) {
            case "Celcius":
                return (suhu - 32) * 5 / 9;
            case "Kelvin":
                return (suhu - 32) * 5 / 9 + 273.15;
            case "Reamur":
                return (suhu - 32) * 4 / 9;
            default:
                return suhu;
        }
    }

// Fungsi konversi dari Kelvin
    private double konversiKelvin(double suhu, String ke) {
        switch (ke) {
            case "Celcius":
                return suhu - 273.15;
            case "Fahrenheit":
                return (suhu - 273.15) * 9 / 5 + 32;
            case "Reamur":
                return (suhu - 273.15) * 4 / 5;
            default:
                return suhu;
        }
    }

// Fungsi konversi dari Reamur
    private double konversiReamur(double suhu, String ke) {
        switch (ke) {
            case "Celcius":
                return suhu * 5 / 4;
            case "Fahrenheit":
                return (suhu * 9 / 4) + 32;
            case "Kelvin":
                return (suhu * 5 / 4) + 273.15;
            default:
                return suhu;
        }
    }

