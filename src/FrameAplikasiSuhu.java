    private void konversiOtomatis() {
        try {
            if (txtSuhu.getText().isEmpty()) {
                jTextField2.setText("");
                return;
            }

            // Ambil nilai suhu dari txtSuhu
            double suhuAwal = Double.parseDouble(txtSuhu.getText());
            String satuanAwal = "";
            String satuanTujuan = cbKonversi.getSelectedItem().toString();
            double suhuHasil = 0;

            // Tentukan satuan awal berdasarkan radiobutton yang dipilih
            if (rbCelcius.isSelected()) {
                satuanAwal = "Celcius";
            } else if (rbFahrenheit.isSelected()) {
                satuanAwal = "Fahrenheit";
            } else if (rbKelvin.isSelected()) {
                satuanAwal = "Kelvin";
            } else if (rbReamur.isSelected()) {
                satuanAwal = "Reamur";
            } else {
                jTextField2.setText("Pilih satuan suhu awal!");
                return;
            }

            // Lakukan konversi berdasarkan satuan awal dan tujuan
            switch (satuanAwal) {
                case "Celcius":
                    suhuHasil = konversiCelcius(suhuAwal, satuanTujuan);
                    break;
                case "Fahrenheit":
                    suhuHasil = konversiFahrenheit(suhuAwal, satuanTujuan);
                    break;
                case "Kelvin":
                    suhuHasil = konversiKelvin(suhuAwal, satuanTujuan);
                    break;
                case "Reamur":
                    suhuHasil = konversiReamur(suhuAwal, satuanTujuan);
                    break;
            }

            // Tampilkan hasil di jTextField2
            jTextField2.setText(String.format("%.2f", suhuHasil));
        } catch (NumberFormatException e) {
            jTextField2.setText("Masukkan angka valid!");
        }
    }

